package com.vivek.vVidLib.controller;

import javax.validation.Valid;

import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.context.ApplicationContext;
import org.springframework.web.bind.annotation.GetMapping;
import org.springframework.web.bind.annotation.RequestBody;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RestController;

import com.fasterxml.jackson.databind.ObjectMapper;
import com.vivek.vVidLib.models.InitializerDto;
import com.vivek.vVidLib.service.SampleVIdService;

@RestController
@RequestMapping("")
public class SampleVIdController {
	@Autowired
	ApplicationContext context;

	@Autowired
	ObjectMapper objectMapper;

	@Autowired
	SampleVIdService sampleVIdService;

	@GetMapping(path = "/getVId")
	public String getVId(@RequestBody @Valid InitializerDto dto) {

		return sampleVIdService.getVId(dto, context, objectMapper);
	}
}
